<h2>The Basic Requirements</h2>
<p>The basics of opening an offshore bank account with us are similar to opening a bank account in your home country. Offshore banks require your personal information, such as your name, date of birth, address, citizenship, and occupation.</p>

<p>To verify your personal information, you will need to submit a copy of your passport, driver's license, or other identifying documents issued by a governmental agency. We are also concerned with verifying your residence or physical address since this may affect taxation issues. This requirement may be satisfied by presenting a utility bill or something similar.</p>

<p>Regardless of what type of document you provide, we will need to make sure they're authentic. A notarized copy of certain documents may suffice in some cases. But other offshore centers prefer an apostilles stamp—a special type of certification mark that's used internationally. If this is the case, you will need to visit the government office that is authorized to issue this stamp for your state or nation.</p>

<h2>Additional Verification Documents</h2>
<p>There are often considerable additional requirements to open an account with us. These requirements are in place to discourage money laundering, tax fraud, or other illegal activities often associated with offshore banking.</p>

<h2>Depositing into your Offshore Business/savings account </h2>

<p>Offshore bank accounts are most often funded electronically through international wire transfers. Unfortunately, the systems that enable free electronic transfers common in domestic banking are typically not able to transfer money internationally.</p>

<h2>Making Withdrawals</h2>

<p>We offer a variety of ways to withdraw funds to maximize the convenience of using their services. We issue a debit card that allows you to easily access your funds worldwide. Make sure you're aware of the fees for using the card since they can add up. Withdrawing larger amounts of cash at one time may help to minimize these fees.

<p>We may also offer checks. However, this is usually not a preferred method– primarily, because confidentiality is often desired in our accounts. Problems may also arise since checks drawn on foreign accounts are not always accepted locally.</p>

